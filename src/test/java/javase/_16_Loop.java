package javase;


import java.util.Scanner;

public class _16_Loop {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++){
            System.out.print(i + " ");
        }

        System.out.println("\n=========");
        int k = 1;
        while (k <= 10){
            System.out.print(k + " ");
            k++;
        }
    }
}
